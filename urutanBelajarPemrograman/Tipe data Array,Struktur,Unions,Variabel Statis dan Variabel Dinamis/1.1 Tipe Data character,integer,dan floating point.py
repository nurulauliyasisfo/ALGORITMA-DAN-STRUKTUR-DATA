# Tipe Data Character, Integer, dan Floating Point

# Tipe Data Character (di Python berupa string dengan 1 karakter)
char_data = 'A'         # character
print("Tipe Data Character:", char_data, "| tipe:", type(char_data))

# Tipe Data Integer (bilangan bulat)
integer_data = 25
print("Tipe Data Integer:", integer_data, "| tipe:", type(integer_data))

# Tipe Data Floating Point (bilangan pecahan/desimal)
float_data = 3.14
print("Tipe Data Floating Point:", float_data, "| tipe:", type(float_data))

# Contoh operasi
print("\nContoh Operasi:")
print("Integer + Integer:", integer_data + 10)
print("Float + Float:", float_data + 2.5)
print("Character (string) + string:", char_data + "BC")

